export function getExerciseName(englishName: string, locale: string): string {
  if (locale === 'ru' && EXERCISE_NAMES_RU[englishName]) {
    return EXERCISE_NAMES_RU[englishName];
  }
  return englishName;
}

export function getExerciseInstructions(englishName: string, locale: string, englishInstructions: string): string {
  if (locale === 'ru' && EXERCISE_INSTRUCTIONS_RU[englishName]) {
    return EXERCISE_INSTRUCTIONS_RU[englishName];
  }
  return englishInstructions;
}
